namespace F0ska\AutoGridTestBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use F0ska\AutoGridTestBundle\Repository\BlogArticleTagExampleRepository;

#[ORM\Entity(repositoryClass: BlogArticleTagExampleRepository::class)]
class BlogArticleTagExample
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 32)]
    private ?string $name = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): static
    {
        $this->name = $name;

        return $this;
    }
}
